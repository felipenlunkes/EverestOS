;; Tocar um som
;; Entrada:
;;			Número da frequência em AX

SOM_tocar_nota:

	pusha
	
	push ax
	mov al, 182
	out 43h, al		;; COnfigurar auto-falante para tocar nota
	pop ax
	
	out 42h, al		;; Byte menos significante
	mov al, ah
	out 42h, al		;; Byte mais significante
	
	in al, 61h
	or al, 00000011b
	out 61h, al		
	
	popa
	ret

;;*****************************************************************************
	

SOM_parar:

	pusha
	
	in al, 61h
	and al, 11111100b
	out 61h, al		;; Serando os dois últimos bytes para o som
	
	popa
	ret
	